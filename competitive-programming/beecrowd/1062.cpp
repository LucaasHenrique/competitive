#include <bits/stdc++.h>
#include <stack>

using namespace std;

int main (int argc, char *argv[]) {
    
    int N;
        

    while (true) {

        cin >> N;

        while (true) {
            int n; 
            stack<int> st;
            
            cin >> n;
            st.push(n);

            if (n == 0) break;
        }

        if (N == 0) break;
    }

    return 0;
}
